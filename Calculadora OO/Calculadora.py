class Operacoes:
    def __init__(self, n1, n2):
        self.n1 = n1                        # numero 1
        self.n2 = n2                        # numero 2

    def soma(self, n1, n2):                 # operacao de adicao
        return self.n1 + self.n2

    def subtracao(self, n1, n2):            # operacao de subtracao
        return self.n1 - self.n2

    def multiplicacao(self, n1, n2):        # operacao de multiplicacao
        return self.n1 * self.n2

    def divisao(self, n1, n2):              # operacao de divisao
        return self.n1 / self.n2

